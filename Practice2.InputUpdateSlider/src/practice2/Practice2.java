    private void tfTempDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfTempDisplayActionPerformed
        String str = tfTempDisplay.getText();
        int num = 0;
        try
        {
            num = Integer.parseInt(str);
        }
        catch(NumberFormatException ex)
        {
             JOptionPane.showMessageDialog(this, "This value is not valid, it must be a number",                   
                    "Input error", JOptionPane.ERROR_MESSAGE); // the second "" is the window title
             return;
        }
        if(num < sliderTemp.getMinimum())
        {
            num = sliderTemp.getMinimum();
        }
        if(num > sliderTemp.getMaximum())
        {
            num = sliderTemp.getMaximum();
        }
                
        sliderTemp.setValue(num);
        
      
        
            
        
    }//GEN-LAST:event_tfTempDisplayActionPerformed

