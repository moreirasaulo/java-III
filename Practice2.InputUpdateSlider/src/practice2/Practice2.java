    private void btRegisterMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegisterMeActionPerformed
        String name = tfName.getText();
        if(!name.matches("[a-zA-Z- ]{1,50}"))
        {
            JOptionPane.showMessageDialog(this, "The name must contain (A to Z) letters and between 1 and 50 characters.", 
                    "Input error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        
        String age = "";
        if(rbBelow18.isSelected())
        {
            age = "Below 18";
        }
        else if(rb18to65.isSelected())
        {
            age = "18 to 65";
        }
        else if(rbOver65.isSelected())
        {
            age = "Over 65";
        }
        else
        { // should never happen, internal error
            JOptionPane.showMessageDialog(this, "Unable to find selected radio button.", "Internal error",
                    JOptionPane.ERROR_MESSAGE);
        }
        
        ArrayList <String> petsList = new ArrayList<>();        
        if(cbPetsDogs.isSelected())
        {
            petsList.add("dogs");
        }
        if(cbPetsCats.isSelected())
        {
            petsList.add("cats");
        }
        if(cbPetsOther.isSelected())
        {
            petsList.add("other");
        }
        String pets = String.join(", ", petsList); // To grab the array and turn into a String
        // with comma and space in between the elements
        
        // Combo is an object, so it needs toString()
        String continent = comboContinents.getSelectedItem().toString();
        
        int prefTemp = sliderTemp.getValue();
        
        taOutput.append(name + ", " + age + ", " + pets + ", " + continent + ", " + prefTemp + " °C\n");
    }//GEN-LAST:event_btRegisterMeActionPerformed

