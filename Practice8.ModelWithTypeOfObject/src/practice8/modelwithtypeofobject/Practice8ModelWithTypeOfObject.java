    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        String colour = tfColour.getText();    
        int age;
        try
        {
            age = Integer.parseInt(tfAge.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(this, "It has to be a number.", "Input Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Cat cat1 = new Cat(colour, age);
        modelCatList.addElement(cat1);
    }//GEN-LAST:event_btAddActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        if(modelCatList.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "There is no content to be deleted", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(lstOutput.getSelectedIndex() == -1)
        {
            JOptionPane.showMessageDialog(this, "Nothing was selected", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        modelCatList.removeElementAt(lstOutput.getSelectedIndex());
        
    }//GEN-LAST:event_btDeleteActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        if(modelCatList.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "There is no content to be updated", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(lstOutput.getSelectedIndex() == -1)
        {
            JOptionPane.showMessageDialog(this, "Select an option first", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        String colour = tfColour.getText();
        
        int age;
        try
        {
            age = Integer.parseInt(tfAge.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(this, "It has to be a number.", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        } 
        
        Cat cat1 = new Cat(colour, age);
        modelCatList.setElementAt(cat1, lstOutput.getSelectedIndex());
    }//GEN-LAST:event_btUpdateActionPerformed

